package com.tst.common;

// 정수, 문자
public class ShareObject {
	/*
	 * Field
	 */
	private int cnt;
	private String str;
		
	/*
	 * Constructor
	 */

	/*
	 * Method
	 */
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	

}
